// Arithmetic Operators
let a = 10;
let b = 2;

console.log("a+b = ", a+b);
console.log("a-b = ", a-b);
console.log("a*b = ", a*b);
console.log("a/b = ", a/b);
console.log("a**b = ", a**b);

// Logical Operators
let x =true;
let y = false;

console.log("x && y =", x && y); // logical AND
console.log("x || y =", x || y); // logical OR
console.log("!x =", !x); // logical NOT