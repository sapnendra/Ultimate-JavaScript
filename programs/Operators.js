// Arithmetic Operators
let a = 10;
let b = 2;

console.log("a+b = ", a+b);
console.log("a-b = ", a-b);
console.log("a*b = ", a*b);
console.log("a/b = ", a/b);
console.log("a**b = ", a**b);

console.log("a++ = ", a++);
console.log("a-- = ", a--);
console.log("++a = ", ++a);
console.log("--a = ", --a);

// Assignment Operators
console.log("a += 10: ", a += 10);
console.log("a -= 10: ", a -= 10);
console.log("a *= 10: ", a *= 10);
console.log("a /= 10: ", a /= 10);
console.log("a %= 10: ", a %= 10);

// Logical Operators
let x =true;
